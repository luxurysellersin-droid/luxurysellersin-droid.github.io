// Add counter animation for stats
function animateCounter(element, target, duration = 2000) {
    let start = 0;
    const increment = target / (duration / 16);
    
    function updateCounter() {
        start += increment;
        if (start < target) {
            element.textContent = Math.floor(start);
            requestAnimationFrame(updateCounter);
        } else {
            element.textContent = target;
        }
    }
    
    updateCounter();
}

// Initialize counter animations when stats section is visible
const statsObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const totalEntries = document.getElementById('totalEntries');
            const totalSpeakers = document.getElementById('totalSpeakers');
            
            if (totalEntries) animateCounter(totalEntries, 8);
            if (totalSpeakers) animateCounter(totalSpeakers, 5);
            
            statsObserver.unobserve(entry.target);
        }
    });
}, { threshold: 0.5 });

const statsSection = document.querySelector('.transcript-stats');
if (statsSection) {
    statsObserver.observe(statsSection);
}
